interface LangCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, fallback?: string) => string;
}

const LanguageContext = createContext<LangCtx>({ lang: 'fr', setLang: () => {}, t: (k) => k });

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>(() => (localStorage.getItem('sad-lang') as Lang) || 'fr');
  const setLang = useCallback((l: Lang) => { localStorage.setItem('sad-lang', l); setLangState(l); }, []);
  const t = useCallback((key: string, fallback?: string) => {
    const entry = DICT[key];
    return entry ? entry[lang] : (fallback ?? key);
  }, [lang]);
  return <LanguageContext.Provider value={{ lang, setLang, t }}>{children}</LanguageContext.Provider>;
};

export const useT = () => useContext(LanguageContext);
